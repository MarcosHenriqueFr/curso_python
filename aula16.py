# Organizado por hierarquia de TAB

entrada = input('Você quer "entrar" ou "sair"? ').lower()

if entrada == 'entrar':
    print('Você entrou no programa!')
elif entrada == 'sair':
    print('Você saiu do programa!')
else:
    print('Não escolheu uma opção!')
