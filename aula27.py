# Fatiamento de strings
# len retorna a quantidade de caracteres, aqui é uma função e não um atributo
# fatiamento [inicio:fim:passos] -> passo vai pular, pode ser negativo também
# final não é incluído

variavel = 'Bom dia'

print(variavel[0:5:1])
print(len(variavel)) # Usar somente com strings por enquanto