# Operadores aritméticos, bem parecido com outras linguagens
# Diferença -> divisão inteira //, deixa ponto flutuante no tipo mas não retorna casa decimal
# divisão sempre retorna número flutuante

adicao = 10+10
print('Adição:', adicao)

subtracao = 10 - 2
print('Subtração:', subtracao)

multiplicacao = 10 * 2
print('Multiplicação:', multiplicacao)

divisao = 10 / 2.2
print('Divisão:', divisao)

divisao_inteira = 10//2.2
print('Divisão inteira:', divisao_inteira)

#Tomar cuidado por causa da memóri do computador
exponenciacao = 2 ** 10 
print('Exponenciação:', exponenciacao)

#Bom para saber múltiplos
modulo = 20 % 2
print('Resto da divisão:', modulo)